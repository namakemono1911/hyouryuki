#ifndef ROCK_H
#define ROCK_H

//////////////////////////////////////////////////////////////////////////////
//ヘッダーファイルインクルード
//////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
//定数定義
//////////////////////////////////////////////////////////////////////////////

//////////////////////////////////////////////////////////////////////////////
//構造体定義
//////////////////////////////////////////////////////////////////////////////
typedef	enum
{
	ROCK_TYPE_000 = 0,			//岩1
	ROCK_TYPE_MAX,				//モデル種類の最大数
	ROCK_TYPE_NULL
}ROCK_TYPE;

typedef struct
{
	D3DXVECTOR3		pos;		//座標
	D3DXVECTOR3		angle;		//角度
	D3DXVECTOR3		size;		//サイズ
	D3DXVECTOR3		collision;	//コリジョン							
	bool			use;		//使用フラグ
	ROCK_TYPE		type;		//モデルの種類
	int				collisionID;//コリジョンID
}ROCK;

//////////////////////////////////////////////////////////////////////////////
//プロトタイプ宣言
//////////////////////////////////////////////////////////////////////////////
void	initRock	( void );
void	uninitRock	( void );
void	updateRock	( void );
void	drawRock	( void );
ROCK	*getRock	( int num );
void	setRock		( D3DXVECTOR3 pos, D3DXVECTOR3 size, D3DXVECTOR3 angle, ROCK_TYPE type);
void	releaseRock	( int num );

#endif